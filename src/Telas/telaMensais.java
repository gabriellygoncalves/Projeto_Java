    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome, mes;
        
        int agua = Integer.parseInt(txtAgua.getText());
        int luz = Integer.parseInt(txtLuz.getText());
        int prestacao = Integer.parseInt(txtPA.getText());
        int Mercado = Integer.parseInt(txtMercado.getText());
        int Outras = Integer.parseInt(txtOutras.getText());
        
        
        nome = txtNome.getText();
        mes = txtMes.getText();
        
        Despesas objdespesas = new Despesas();
        
        objdespesas.setNome_user(nome);
        objdespesas.setMes_user(mes);
        objdespesas.setAgua_despesa(agua);
        objdespesas.setLuz_despesa(luz);
        objdespesas.setAluguel_prestaçao(prestacao);
        objdespesas.setSupermercado_despesa(Mercado);
        objdespesas.setOutras_despesas(Outras);
        
        Conexao_sql objConexaosql = new Conexao_sql();
       
       objConexaosql.despesasPessoais(objdespesas);
        
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

