private void DespesaMensal(){
    try {
        Conexao_sql objconexaosql = new Conexao_sql();
        
        DefaultTableModel model = (DefaultTableModel) tblMensal.getModel();
        model.setNumRows(0);
        
        ArrayList<Despesas> lista = objconexaosql.tbMensal();
        
        for(int num = 0 ; num < lista.size(); num++){
            model.addRow(new Object[]{  
            
                    lista.get(num).getId_user(),
                    lista.get(num).getNome_user(),
                    lista.get(num).getMes_user(),
                    lista.get(num).getAgua_despesa(),
                    lista.get(num).getLuz_despesa(),
                    lista.get(num).getAluguel_prestaçao(),
                    lista.get(num).getSupermercado_despesa(),
                    lista.get(num).getOutras_despesas()       
            });
        }
        
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(null, "Despesa Mensal" + erro);
    }
}
